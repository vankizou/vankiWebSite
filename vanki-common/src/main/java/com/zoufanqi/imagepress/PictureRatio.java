package com.zoufanqi.imagepress;

public class PictureRatio {
    private Integer ratioWidth;     // 计算出的比例宽
    private Integer ratioHeight;    // 计算出的比例高

    public Integer getRatioWidth() {
        return ratioWidth;
    }

    public void setRatioWidth(Integer ratioWidth) {
        this.ratioWidth = ratioWidth;
    }

    public Integer getRatioHeight() {
        return ratioHeight;
    }

    public void setRatioHeight(Integer ratioHeight) {
        this.ratioHeight = ratioHeight;
    }
}