package com.zoufanqi.entity;

import com.fasterxml.jackson.annotation.JsonIgnore;

import java.util.Date;

public class Picture {
    private Long id;

    private String uuid;

    private Long userId;

    private String name;

    private String path;

    private Integer width;

    private Integer height;

    private Long size;

    private String type;

    private Integer useType;

    private Date crateDatetime;

    @JsonIgnore
    private Integer isDel;

    public boolean validate() {
        if (this.uuid == null || userId == null || path == null) return false;
        return true;
    }


    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public Integer getWidth() {
        return width;
    }

    public void setWidth(Integer width) {
        this.width = width;
    }

    public Integer getHeight() {
        return height;
    }

    public void setHeight(Integer height) {
        this.height = height;
    }

    public Long getSize() {
        return size;
    }

    public void setSize(Long size) {
        this.size = size;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Integer getUseType() {
        return useType;
    }

    public void setUseType(Integer useType) {
        this.useType = useType;
    }

    public Date getCrateDatetime() {
        return crateDatetime;
    }

    public void setCrateDatetime(Date crateDatetime) {
        this.crateDatetime = crateDatetime;
    }

    public Integer getIsDel() {
        return isDel;
    }

    public void setIsDel(Integer isDel) {
        this.isDel = isDel;
    }
}